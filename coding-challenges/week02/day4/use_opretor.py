# Write a program to take in 2 numbers as input and print their +, -, /, //, % and *

a = int(input("Enter a Value "))

b = int(input("Enter b Value "))

print("Addition = ", a + b)
print("Subtraction = ", a - b)
print("Division = ", a / b)
print("Integer Division = ", a // b)
print("Addition = ", a % b)
print("Multiplication = ", a * b)
